package com.example.cineplanet;

class SliderItem {
    final String imageUrl;
    final String title;
    final String subtitle;
    final String promoLabel;

    SliderItem(String imageUrl, String title, String subtitle, String promoLabel) {
        this.imageUrl = imageUrl;
        this.title = title;
        this.subtitle = subtitle;
        this.promoLabel = promoLabel;
    }
}
